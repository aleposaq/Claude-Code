/*
 * PRISM v2 build: validate the v2 corpus and emit js/levels2.js.
 * Bakes in `par` (the score to beat) and a `live` difficulty signal. Refuses to
 * emit if any level is unsolvable or its search truncated (honest QA gate).
 */
const fs = require('fs');
const path = require('path');
const { solve, par, liveFirstMoves } = require('./solver2.js');
const DESIGNS = require('./leveldata2.js');

const out = [];
const report = [];
let fail = 0;

DESIGNS.forEach((d, i) => {
  const p = par(d);
  const live = liveFirstMoves(d);
  const res = solve(d, 2);
  const flags = [];
  if (!res.solutions.length) { flags.push('UNSOLVABLE'); fail++; }
  if (p.truncated || res.truncated) { flags.push('TRUNCATED'); fail++; }

  const level = Object.assign({ id: i + 1, par: p.par, live: live.live }, d);
  out.push(level);
  report.push(`#${String(i + 1).padStart(2)} ${d.name.padEnd(16)} ${d.w}x${d.h} par:${p.par} live:${live.live}/${live.total} ${flags.join(' ') || 'ok'}`);
});

console.log(report.join('\n'));
console.log(`\n${out.length} levels | ${fail} problems`);

if (process.argv.includes('--emit')) {
  if (fail) { console.error('Refusing to emit: problems exist.'); process.exit(1); }
  const banner = '/* AUTO-GENERATED by tools/build2.js from tools/leveldata2.js — do not edit by hand. */\n';
  const body = '(function (root, factory) {\n' +
    '  if (typeof module !== "undefined" && module.exports) module.exports = factory();\n' +
    '  else root.LEVELS2 = factory();\n' +
    '}(typeof self !== "undefined" ? self : this, function () {\n' +
    '  return ' + JSON.stringify(out) + ';\n' +
    '}));\n';
  fs.writeFileSync(path.join(__dirname, '..', 'js', 'levels2.js'), banner + body);
  console.log('Wrote js/levels2.js');
}
